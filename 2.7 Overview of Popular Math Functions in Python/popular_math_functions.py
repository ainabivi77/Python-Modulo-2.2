import math

loss = -20.25
product_cost = 89.99

print(abs(loss))  # valor absoluto, toma el valor puro, nos quita el negativo, no necesita la librería 
print(math.floor(product_cost))# redondea hacia abajo
print(math.ceil(product_cost))# redondea hacia arriba
print(abs(math.floor(loss))) # combinar funciones
print(round(product_cost)) # redondea al entero más cercano
print(math.sqrt(product_cost))# raíz cuadrada
print(math.pow(5, 2))# al cuadrado
print(5**2)#exponent operator